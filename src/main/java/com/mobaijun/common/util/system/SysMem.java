package com.mobaijun.common.util.system;

/**
 * software：IntelliJ IDEA 2022.1
 * class name: SysMem
 * class description： 系统内存相关信息
 *
 * @author MoBaiJun 2022/5/11 14:54
 */
public class SysMem {
    /**
     * 内存总量
     */
    private double total;

    /**
     * 已用内存
     */
    private double used;

    /**
     * 剩余内存
     */
    private double free;

    public double getTotal() {
        return Arity.div(total, (1024 * 1024 * 1024), 2);
    }

    public void setTotal(long total) {
        this.total = total;
    }

    public double getUsed() {
        return Arity.div(used, (1024 * 1024 * 1024), 2);
    }

    public void setUsed(long used) {
        this.used = used;
    }

    public double getFree() {
        return Arity.div(free, (1024 * 1024 * 1024), 2);
    }

    public void setFree(long free) {
        this.free = free;
    }

    public double getUsage() {
        return Arity.mul(Arity.div(used, total, 4), 100);
    }
}